module Mlatu.Metadata
  ( Metadata (..),
    name,
    fields,
    origin,
  )
where

import Data.HashMap.Strict qualified as HashMap
import Mlatu.Name (GeneralName, Unqualified)
import Mlatu.Origin (Origin)
import Mlatu.Term (Term)
import Optics.TH (makeLenses)
import Optics (view)
import Relude
import Text.PrettyPrint qualified as Pretty
import Text.PrettyPrint.HughesPJClass (Pretty (..))

-- | Untyped metadata from @about@ blocks.
data Metadata = Metadata
  { _fields :: !(HashMap Unqualified (Term ())),
    _name :: !GeneralName,
    _origin :: !Origin
  }
  deriving (Show)

makeLenses ''Metadata

instance Pretty Metadata where
  pPrint metadata =
    Pretty.vcat
      [ Pretty.hcat ["about ", pPrint $ view name metadata, ":"],
        Pretty.nest 4 $
          Pretty.vcat $
            map field $
              HashMap.toList $ view fields metadata
      ]
    where
      field (key, value) =
        Pretty.vcat
          [ Pretty.hcat [pPrint key, ":"],
            Pretty.nest 4 $ pPrint value
          ]
